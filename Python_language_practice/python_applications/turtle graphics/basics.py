import turtle as t
t.Turtle()
t.goto(10,10)
t.goto(50,50)
t.penup()
t.goto(-100,-100)
t.pendown()
t.goto(-50,-50)
t.goto(-10,-10)
t.color('blue')
t.pensize(5)
t.goto(0,0)
t.speed(50)
t.goto(200,-200)
t.goto(-200,200)
t.speed(1)
t.getscreen().bgcolor("yellow")
t.color('red')
t.forward(300)
t.left(100)
t.done()